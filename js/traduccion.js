// Function to set language and translate
function setLanguage(lang) {
    // Validate if language exists, otherwise default to 'es'
    if (!translations[lang]) lang = 'es';
    
    // Save selection in localStorage
    localStorage.setItem('preferred-lang', lang);
    
    // Update HTML lang attribute
    document.documentElement.setAttribute('lang', lang);
    
    // Translate text of elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Check if element is an input/textarea (needs placeholder translation)
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.setAttribute('placeholder', translations[lang][key]);
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update active value on select tags (sync inputs if there are multiple selector widgets)
    document.querySelectorAll('.lang-selector').forEach(selector => {
        selector.value = lang;
    });
}

// Initialize on DOM load
document.addEventListener('DOMContentLoaded', () => {
    // Get language from localStorage, or use navigator language, defaulting to 'es'
    const savedLang = localStorage.getItem('preferred-lang');
    const browserLang = navigator.language.slice(0, 2);
    const defaultLang = savedLang || (translations[browserLang] ? browserLang : 'es');
    
    setLanguage(defaultLang);
});
